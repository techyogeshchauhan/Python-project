# Get two values from the user
a = input("Enter the first value (a): ")
b = input("Enter the second value (b): ")

# Display the original values
print("Before swapping:")
print("a =", a)
print("b =", b)

# Swap the values
a, b = b, a

# Display the swapped values
print("After swapping:")
print("a =", a)
print("b =", b)
